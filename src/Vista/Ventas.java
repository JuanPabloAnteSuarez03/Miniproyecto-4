    private void jTableProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductoMouseClicked
        // TODO add your handling code here:
        int seleccion = jTableProducto.rowAtPoint(evt.getPoint());
        jTextId.setText(String.valueOf(jTableProducto.getValueAt(seleccion, 0)));
        jTextNombre.setText(String.valueOf(jTableProducto.getValueAt(seleccion, 2)));
        jTextPrecio.setText(String.valueOf(jTableProducto.getValueAt(seleccion, 4)));
        codigo = Integer.parseInt(String.valueOf(jTableProducto.getValueAt(seleccion, 0)));
    }//GEN-LAST:event_jTableProductoMouseClicked

